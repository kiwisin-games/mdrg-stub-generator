    "Food",
    "FoodCookedDish",
    "Usable",
    "Package",
    "Room",
    "Modules",
    "Fish",
    "CookingIngredient",
    "FishingTrash",
    "Meds",
    "Soap",
    "AnonEquip",
    "Other",
